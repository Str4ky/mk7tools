let currentAudio = null;
let currentButton = null;

function play(path, button) {
  if (currentAudio) {
    currentAudio.pause();
    currentAudio.currentTime = 0;

    if (currentButton) {
      currentButton.classList.remove("fa-stop");
      currentButton.classList.add("fa-play");

    }

    if (currentAudio.src === new Audio(path).src) {
      currentAudio = null;
      currentButton = null;
      return;
    }
  }

  currentAudio = new Audio(path);
  currentAudio.volume = 0.2;
  currentAudio.play();

  currentButton = button;
  currentButton.classList.remove("fa-play");
  currentButton.classList.add("fa-stop");

  currentAudio.onended = function () {
    if (currentButton) {
      currentButton.classList.remove("fa-stop");
      currentButton.classList.add("fa-play");
    }
    currentAudio = null;
    currentButton = null;
  };
}