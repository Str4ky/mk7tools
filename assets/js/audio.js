function generateVoicelines(character) {
  const container = document.getElementById(character);
  if (!container) return;

  container.innerHTML = "";

  if (!voicelines[character]) return;

  voicelines[character].forEach(section => {
    const area = document.createElement("div");
    area.classList.add("collapsible-area");
    area.innerHTML = `<h3>${section.category}</h3>`;

    section.lines.forEach(line => {
      const div = document.createElement("div");
      div.classList.add("collapsible-infos");
      div.innerHTML = `
        <i onclick="play('../assets/audio/${character}/${section.prefix}/${line.id}.wav', this)" class="fa-solid fa-play"></i>
        <p>${line.id} ${line.name}</p>
      `;
      area.appendChild(div);
    });

    container.appendChild(area);
  });
}

